public class Aritmatika {
    public static void main(String[] args) {
        // Variabel
        int angka1 = 10;
        int angka2 = 3;
        
        // Penambahan
        int hasilPenambahan = angka1 + angka2;
        System.out.println("Hasil Pertambahan A+B = " + hasilPenambahan);
        
        // Pengurangan
        int hasilPengurangan = angka1 - angka2;
        System.out.println("Hasil Pengurangan A-B = " + hasilPengurangan);
        
        // Perkalian
        int hasilPerkalian = angka1 * angka2;
        System.out.println("Hasil Perkalian A*B = " + hasilPerkalian);
        
        // Pembagian
        double hasilPembagian = (double) angka1 / angka2;
        System.out.println("Hasil Pembagian A/B = " + hasilPembagian);
    }
}