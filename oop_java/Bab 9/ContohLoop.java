public class ContohLoop {

    public static void main(String[] args) {
        // Inisialisasi variabel i
        int i = 0;

        // Loop dengan kondisi i < 6
        while (i < 6) {
            // Menampilkan nilai i
            System.out.print(i + " ");
            // Increment nilai i
            i++;
        }
    }
}